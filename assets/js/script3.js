function suma() {
    var valor1 = document.getElementById('valor1').value;
    var valor2 = document.getElementById('valor2').value;
    alert(parseInt(valor1) + parseInt(valor2));
    return parseInt(valor1) + parseInt(valor2);

}

function resta() {
    var valor1 = document.getElementById('valor1').value;
    var valor2 = document.getElementById('valor2').value;
    alert(parseInt(valor1) - parseInt(valor2));
    return parseInt(valor1) - parseInt(valor2);

}